package com.class03;

public class CompoundOperator {
	public static void main(String[] args) {
	
		int num=100;
		num=num+100;
		
		System.out.println(num);
		num=num-50;
		
		System.out.println(num);
		System.out.println("--------------------------------");
		
		int num1=100;
		num1+=100;//num1+num1+100;   //ADDITION
		System.out.println(num1);
		
		num1-=50;                   //SUBTRACTION
		System.out.println(num1);
				
		num1/=5;                   //DIVISION
		System.out.println(num1);
		
		
		num1*=10;
		System.out.println(num1);  //MULTIPLICATION
		
		num1+=20;
		System.out.println(num1);  //ADDING TO LAST NUM1 made
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
}
}